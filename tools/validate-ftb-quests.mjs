import { createHash } from "node:crypto";
import { readdirSync, readFileSync, statSync } from "node:fs";
import { join, relative } from "node:path";

const liveRoot = "config/ftbquests/quests";
const overrideRoot = "overrides/config/ftbquests/quests";

function fail(message) {
  throw new Error(message);
}

function filesUnder(root) {
  const result = [];
  const visit = (directory) => {
    for (const name of readdirSync(directory)) {
      const path = join(directory, name);
      if (statSync(path).isDirectory()) {
        visit(path);
      } else {
        result.push(relative(root, path).replaceAll("\\", "/"));
      }
    }
  };
  visit(root);
  return result.sort();
}

function text(path) {
  return readFileSync(path, "utf8");
}

function sha256(path) {
  return createHash("sha256").update(readFileSync(path)).digest("hex");
}

function validateDelimiters(path, source) {
  const stack = [];
  let inString = false;
  let escaped = false;
  for (let index = 0; index < source.length; index++) {
    const character = source[index];
    if (inString) {
      if (escaped) {
        escaped = false;
      } else if (character === "\\") {
        escaped = true;
      } else if (character === '"') {
        inString = false;
      }
      continue;
    }
    if (character === '"') {
      inString = true;
    } else if (character === "{" || character === "[") {
      stack.push(character);
    } else if (character === "}" || character === "]") {
      const expected = character === "}" ? "{" : "[";
      if (stack.pop() !== expected) {
        fail(`${path}: mismatched ${character} delimiter`);
      }
    }
  }
  if (inString || stack.length > 0) {
    fail(`${path}: unclosed string or delimiter`);
  }
}

const liveFiles = filesUnder(liveRoot);
const overrideFiles = filesUnder(overrideRoot);
if (JSON.stringify(liveFiles) !== JSON.stringify(overrideFiles)) {
  fail("Live and override quest trees do not contain the same files.");
}

for (const relativePath of liveFiles) {
  const livePath = join(liveRoot, relativePath);
  const overridePath = join(overrideRoot, relativePath);
  if (sha256(livePath) !== sha256(overridePath)) {
    fail(`Mirror mismatch: ${relativePath}`);
  }
  validateDelimiters(livePath, text(livePath));
}

const chapterFiles = liveFiles.filter((path) => path.startsWith("chapters/"));
const languageFiles = liveFiles.filter((path) => path.startsWith("lang/"));
if (chapterFiles.length !== 18 || languageFiles.length !== 1 || languageFiles[0] !== "lang/en_us.snbt") {
  fail(`Expected 18 chapters and exactly the installed runtime's flat lang/en_us.snbt file.`);
}

const chapters = chapterFiles.map((path) => text(join(liveRoot, path)));
const chapterText = chapters.join("\n");
const localeText = text(join(liveRoot, "lang/en_us.snbt"));

for (let index = 0; index < localeText.length; index++) {
  if (localeText[index] === "&" && localeText[index - 1] !== "\\") {
    fail("The locale contains an unescaped ampersand that FTB Library will interpret as a formatting code.");
  }
}

const questIds = [...chapterText.matchAll(/^ {6}id: "([0-9A-F]{16})"$/gm)].map((match) => match[1]);
const objectIds = [...chapterText.matchAll(/^ +id: "([0-9A-F]{16})"$/gm)].map((match) => match[1]);
if (questIds.length !== 136) {
  fail(`Expected 136 quests; found ${questIds.length}.`);
}
if (new Set(objectIds).size !== objectIds.length) {
  fail("A chapter, quest, task, or reward object ID is duplicated.");
}
if (objectIds.some((objectId) => BigInt(`0x${objectId}`) > 0x7fffffffffffffffn)) {
  fail("An object ID exceeds the positive signed-long range and will be regenerated by this FTB Quests build.");
}

for (const questId of questIds) {
  if (!localeText.includes(`quest.${questId}.title:`) || !localeText.includes(`quest.${questId}.quest_desc:`)) {
    fail(`Missing title or description for quest ${questId}.`);
  }
}

const dependencyIds = [...chapterText.matchAll(/dependencies: \[([^\]]*)\]/g)]
  .flatMap((match) => [...match[1].matchAll(/"([0-9A-F]{16})"/g)].map((entry) => entry[1]));
const questIdSet = new Set(questIds);
for (const dependencyId of dependencyIds) {
  if (!questIdSet.has(dependencyId)) {
    fail(`Dependency ${dependencyId} does not refer to a quest.`);
  }
}

const data = text(join(liveRoot, "data.snbt"));
if (!data.includes('default_autoclaim_rewards: "disabled"')
  || !data.includes("default_reward_team: false")
  || !data.includes('fallback_locale: "en_us"')) {
  fail("Starter rewards are not configured for manual, per-player claiming.");
}

const rewardCount = (chapterText.match(/type: "item"/g) ?? []).length
  - (chapterText.match(/^ {10}type: "item"$/gm) ?? []).length;
const explicitPersonalRewards = (chapterText.match(/team_reward: false/g) ?? []).length;
if (rewardCount !== 3 || explicitPersonalRewards !== 3) {
  fail(`Expected exactly three personal item rewards; found ${rewardCount} rewards and ${explicitPersonalRewards} personal overrides.`);
}

const welcome = text(join(liveRoot, "chapters/welcome.snbt"));
for (const starter of ["satchels:satchel", "sophisticatedbackpacks:backpack"]) {
  if (!welcome.includes(`item: { count: 1, id: "${starter}" }`)) {
    fail(`Missing Welcome reward: ${starter}`);
  }
}
if (chapterText.includes("antique_atlas:atlas")
  || !welcome.includes('"minecraft:custom_name": "{\\"translate\\":\\"item.antique_atlas.atlas\\"}"')
  || !welcome.includes('count: 1, id: "minecraft:book"')) {
  fail("The Antique Atlas reward is not a valid custom-named vanilla book.");
}
if (welcome.includes('id: "480203E01AA5D755"')) {
  fail("The corrected Antique Atlas reward must use a fresh reward ID.");
}

const groupFile = text(join(liveRoot, "chapter_groups.snbt"));
const orderedGroupIds = [...groupFile.matchAll(/\{ id: "([0-9A-F]{16})" \}/g)].map((match) => match[1]);
const chapterMetadata = new Map(chapterFiles.map((path) => {
  const source = text(join(liveRoot, path));
  return [path.replace(/^chapters\/|\.snbt$/g, ""), {
    group: source.match(/^  group: "([0-9A-F]{16})"$/m)?.[1],
    id: source.match(/^  id: "([0-9A-F]{16})"$/m)?.[1],
    order: Number(source.match(/^  order_index: (\d+)$/m)?.[1]),
  }];
}));
for (const [filename, metadata] of chapterMetadata) {
  if (!orderedGroupIds.includes(metadata.group)) {
    fail(`Chapter ${filename} refers to an unknown chapter group.`);
  }
  if (!localeText.includes(`chapter.${metadata.id}.title:`)) {
    fail(`Chapter ${filename} has no localized title.`);
  }
}
const welcomeMetadata = chapterMetadata.get("welcome");
if (welcomeMetadata.group !== orderedGroupIds[0] || welcomeMetadata.order !== 0
  || chapterMetadata.get("campaign_compass").order !== 1
  || chapterMetadata.get("eyes_of_end").order !== 2) {
  fail("The Welcome, Campaign Compass, and Eye chapters are not ordered first in the first chapter group.");
}
for (const groupId of orderedGroupIds) {
  if (!localeText.includes(`chapter_group.${groupId}.title:`)) {
    fail(`Chapter group ${groupId} has no localized title.`);
  }
}

const eyes = text(join(liveRoot, "chapters/eyes_of_end.snbt"));
if ((eyes.match(/type: "kill"/g) ?? []).length !== 17) {
  fail("The Eye chapter must contain sixteen Eye boss kills and the Ender Dragon kill.");
}
if (!eyes.includes("min_required_dependencies: 12")) {
  fail("The Ender Dragon quest is not configured for twelve of sixteen Eye dependencies.");
}
const dragonDependencies = [...eyes.matchAll(/dependencies: \[([^\]]*)\]/g)]
  .map((match) => [...match[1].matchAll(/"([0-9A-F]{16})"/g)].length)
  .find((count) => count === 16);
if (dragonDependencies !== 16) {
  fail("The Ender Dragon quest does not display all sixteen incoming Eye dependencies.");
}

const sidebar = text("config/ftblibrary-client.snbt");
if (!/"ftbquests:quests"\s*:\s*\{[\s\S]*?enabled:\s*true/.test(sidebar)) {
  fail("The FTB Quests inventory sidebar button is not enabled.");
}

console.log(`Validated ${chapterFiles.length} chapters, ${questIds.length} quests, ${dependencyIds.length} dependencies, and an exact live/override mirror.`);
